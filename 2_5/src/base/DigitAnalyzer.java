package base;

public class DigitAnalyzer {

    public static void main(String[] args) {
        int number = 12345; //исходник для вывода и проверки

        //Первый цикл: сумма цифр, количество цифр и максимальная цифра в числе
        int backup = number; //сохраняем переменную с числом, чтобы менять его в первом цикле
        int sum = 0; //переменная суммы всех цифр
        int count = 0; //переменная количества цифр
        int maxDigit = 0; //переменная максимальной цифры

        while (backup > 0) {
            int digit = backup % 10; //определяем остаток после деления (последнюю цифру)
            sum += digit; //определяем сумму всех цифр в числе
            count++; //определяем количество цифр в числе
            //определяем максимальную цифру
            if (digit > maxDigit) { //если текущая цифра больше максимума
                maxDigit = digit; //то перезаписываем максимум
            }
            backup /= 10; //отбрасываем последнюю цифру, чтоб перейти на следующий круг цикла
        }

        //Второй цикл: проверка на палиндром
        backup = number; //возвращаем цифру, т.к. в первом цикле она уже равна 0
        int reversed = 0;

        while (backup > 0) {
            int digit = backup % 10; //определяем остаток после деления (последнюю цифру)
            reversed = reversed * 10 + digit; //переворачиваем число наоборот
            backup /= 10; //отбрасываем последнюю цифру, чтоб перейти на следующий круг цикла
        }

        boolean isPalindrome = (number == reversed); //определяем, равен ли исходник получившемуся значению

        //Выводим на печать результаты
        System.out.println("Исходное число: " + number);
        System.out.println("Сумма цифр в числе: " + sum);
        System.out.println("Количество цифр в числе: " + count);
        System.out.println("Максимальная цифра в числе: " + maxDigit);
        System.out.println("Является ли число палиндром? " + (isPalindrome ? "да" : "нет"));
    }
}